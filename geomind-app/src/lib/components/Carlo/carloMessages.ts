// ========================================
// Fonction pour obtenir un message aléatoire selon l'expéditeur
// ========================================

export interface TeamsMessage {
  sender: TeamsSender;
  senderInfo: TeamsSenderInfo;
  message: string;
  isAllCaps: boolean;
}

export function getRandomTeamsMessageWithSender(): TeamsMessage {
  // Pondération des expéditeurs - bien équilibrée
  const weights: { sender: TeamsSender; weight: number }[] = [
    { sender: 'carlo', weight: 20 },
    { sender: 'etienne', weight: 20 },
    { sender: 'nathalie', weight: 15 },
    { sender: 'tam', weight: 15 },
    { sender: 'guy', weight: 12 },
    { sender: 'kevin', weight: 18 }
  ];

  const totalWeight = weights.reduce((sum, w) => sum + w.weight, 0);
  let random = Math.random() * totalWeight;

  let selectedSender: TeamsSender = 'carlo';
  for (const w of weights) {
    random -= w.weight;
    if (random <= 0) {
      selectedSender = w.sender;
      break;
    }
  }

  let message: string;
  let isAllCaps = false;

  switch (selectedSender) {
    case 'etienne':
      // 60% demandes (lunettes, croissants, ordres), 40% blagues
      if (Math.random() < 0.6) {
        message = ETIENNE_DEMANDS[Math.floor(Math.random() * ETIENNE_DEMANDS.length)];
      } else {
        message = ETIENNE_JOKES[Math.floor(Math.random() * ETIENNE_JOKES.length)];
      }
      break;

    case 'nathalie':
      message = NATHALIE_FOOD[Math.floor(Math.random() * NATHALIE_FOOD.length)];
      break;

    case 'tam':
      message = TAM_COMPLAINTS[Math.floor(Math.random() * TAM_COMPLAINTS.length)];
      isAllCaps = true;
      break;

    case 'guy':
      message = GUY_CONTRACTS[Math.floor(Math.random() * GUY_CONTRACTS.length)];
      break;

    case 'kevin':
      message = KEVIN_MESSAGES[Math.floor(Math.random() * KEVIN_MESSAGES.length)];
      break;

    case 'carlo':
    default:
      // Carlo: questions absurdes et accusations (pas de messages agressifs)
      const carloPool = [
        ...CARLO_TEAMS_QUESTIONS,
        ...CARLO_TEAMS_ACCUSATIONS,
        ...CARLO_TEAMS_TECH_ABSURD,
        ...CARLO_TEAMS_BFK
      ];
      message = carloPool[Math.floor(Math.random() * carloPool.length)];
      break;
  }

  return {
    sender: selectedSender,
    senderInfo: TEAMS_SENDERS[selectedSender],
    message,
    isAllCaps
  };
}
