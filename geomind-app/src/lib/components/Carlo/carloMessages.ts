// Fonction pour obtenir un message Teams aléatoire selon le niveau d'escalade
export function getRandomTeamsMessage(escalationLevel: number = 1): { message: string; isAngry: boolean } {
  let pool: string[];
  let isAngry = false;

  if (escalationLevel >= 4) {
    pool = CARLO_TEAMS_ANGRY;
    isAngry = true;
  } else if (escalationLevel === 3) {
    pool = [...CARLO_TEAMS_PASSIVE_AGGRESSIVE, ...CARLO_TEAMS_IMPATIENT];
  } else if (escalationLevel === 2) {
    pool = CARLO_TEAMS_IMPATIENT;
  } else {
    // Niveau 1 - messages normaux
    pool = [
      ...CARLO_TEAMS_QUESTIONS,
      ...CARLO_TEAMS_ACCUSATIONS,
      ...CARLO_TEAMS_TECH_ABSURD,
      ...CARLO_TEAMS_BFK
    ];
  }

  return {
    message: pool[Math.floor(Math.random() * pool.length)],
    isAngry
  };
}

